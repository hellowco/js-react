let name = "홍길동";
let a = 15, b = 4;

// 산술 연산
console.log(a + b); // 19
console.log(a - b); // 11
console.log(a * b); // 60
console.log(a / b); // 3.75
console.log(a % b); // 3

// 비교 연산
console.log(a > b);   // true
console.log(a < b);   // false
console.log(a === b); // false
console.log(a !== b); // true

// 템플릿 리터럴
console.log(`안녕하세요, ${name}님! ${a} + ${b} = ${a + b}`);